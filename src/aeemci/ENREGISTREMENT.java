    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        
        String spe=SPECIALITE_BOX.getSelectedItem().toString();
        String parcours;
        if("MATHEMATIQUE".equals(spe) || "GENIE INFORMATIQUE".equals(spe) || "INFORMATIQUE".equals(spe)
                ||"MIAGE".equals(spe) || "MATHS INFO".equals(spe)){
            parcours="MATHS INFO";
        }else if("CHIMIE".equals(spe) || "PHYSIQUE".equals(spe) || "PHYSIQUE-CHIMIE".equals(spe)){
        parcours="PHYSIQUE CHIMIE";
        }else{
            parcours="SN";
     }
        String nce = NCE_TXT.getText();
        String nom =NOM_TXT.getText();
        String prenom = PRENOM_TXT.getText();
        String cell=cell_txt.getText();
        String sexe = SEXE_BOX.getSelectedItem().toString();
        String statut = PERSONNE_fl.getText().toLowerCase();
        String filiere= SPECIALITE_BOX.getSelectedItem().toString();
        String niveau = NIVEAU_BOX.getSelectedItem().toString();
        String parrainer = "NON"; 
        String sql="";
        
        if(nom.contains("'") || prenom.contains("'") || nce.contains("'")){
            nom = nom.replaceAll("'","''");
           // prenom = prenom.replaceAll("'", "''");
            JOptionPane.showMessageDialog(null, "Pour plus de securit√©, veuillez enlever l'apostrophe: '' ' '' ");
        }else{
        
         int entier,nbre=0;
         
         
         
         for(int i=0;i<cell.length();i++){
             for(int j=0;j<=9;j++){
                 char mot=cell.charAt(i);
              
                 
                 entier=Character.getNumericValue(mot);
                 if(j==entier){
                     nbre+=1;
                     }
             }
         }
         
         if(nbre==8 && cell.length()==8 ){
            
       if("etudiant".equals(statut)){
             sql ="INSERT INTO "+statut+"(nce_et,nom_et,prenom_et,sexe,filiere_et,niveau_et,telephone_et,parrainer,parrainer_2)"
                    + " VALUE('"+nce+"','"+nom+"','"+prenom+"','"+sexe+"','"+filiere+"','"+niveau+"','"+cell+"','"+parrainer+"','"+parrainer+"')";
        }else {
              
           sql ="INSERT INTO "+statut+"(nce_par,nom_par,prenom_par,sexe,parcours,filiere_par,niveau_par,telephone_par,parrain_ok,parrain2_ok)"
            + " VALUE('"+nce+"','"+nom+"','"+prenom+"','"+sexe+"','"+parcours+"','"+filiere+"','"+niveau+"','"+cell+"','"+parrainer+"','"+parrainer+"')";
       }
       
        try {
            stmt= maConnexion.ObtenirConnexion().createStatement();
            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, statut+": "+nom+" "+prenom+" ENREGISTRER AVEC SUCCES !!!");
            NCE_TXT.setText("");
            NOM_TXT.setText("");
            PRENOM_TXT.setText("");
            cell_txt.setText("");
            SEXE_BOX.setSelectedIndex(0);
            SPECIALITE_BOX.setSelectedIndex(0);
            NIVEAU_BOX.setSelectedIndex(0);
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "VEILLEZ BIEN REMPLIR TOUS LES CHAMPS"+ex);
        }
         }else{
             JOptionPane.showMessageDialog(null, "SAISSISEZ UN BON NUMERO CELLULAIRE");
         }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        NIVEAU_BOX.removeAllItems();
        SPECIALITE_BOX.removeAllItems();
        NIVEAU_BOX.addItem("NIVEAU");
        SPECIALITE_BOX.addItem("SPECIALITE");
      //  NIVEAU_BOX.rem
        String sql ="SELECT * FROM grade";
        String sql2 ="SELECT * FROM specialite";
        try {
            stmt = maConnexion.ObtenirConnexion().createStatement();
            ResultSet resultat =stmt.executeQuery(sql);
            while(resultat.next()){
                NIVEAU_BOX.addItem(resultat.getString("niveau"));
            }
            resultat.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "ERREUR "+ex);
        }
        
        try {
         stmt= maConnexion.ObtenirConnexion().createStatement();
         ResultSet resultat2 = stmt.executeQuery(sql2);
         while(resultat2.next()){
             SPECIALITE_BOX.addItem(resultat2.getString("filiere"));
         }
         resultat2.close();
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
        
    }//GEN-LAST:event_formComponentShown

